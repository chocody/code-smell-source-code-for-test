from pathlib import Path
from typing import Dict, List


class ReportService:
    def summarize(self, sales_chunks: List[Dict[str, object]], inventory_chunks: List[Dict[str, object]]) -> Dict[str, object]:
        total_revenue = 0.0
        region_totals: Dict[str, float] = {}
        for chunk in sales_chunks:
            total_revenue += float(chunk["total_revenue"])
            for region, value in chunk["regional"].items():
                region_totals[region] = region_totals.get(region, 0.0) + float(value)
        return {
            "total_revenue": total_revenue,
            "region_totals": region_totals,
            "inventory_passes": len(inventory_chunks),
            "merged_chunks": len(sales_chunks) + len(inventory_chunks),
        }

    def write_report(self, path: Path, payload: Dict[str, object]) -> None:
        path.write_text(str(payload), encoding="utf-8")
